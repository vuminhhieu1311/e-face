    public function store(Request $request)
    {
        $message = Message::create([
            'user_id' => Auth::id(),
            'room_id' => $request['room_id'],
            'content' => $request['content'],
        ]);
        $message->load('user');
        broadcast(new SendMessageEvent($message))->toOthers();

        return response()->json([
            'message' => $message,
        ], 200);
    }

